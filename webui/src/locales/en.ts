// Dict widens all leaf strings to `string` so the zh.ts (and any future
// locale) can hold *different* strings for the same keys — otherwise a
// literal-narrowed template would reject every translation. We still get
// structural checking: any missing key or misspelt path from en.ts fails
// the type check.
type WidenStrings<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends object
      ? WidenStrings<T[K]>
      : T[K];
};
export type Dict = WidenStrings<typeof en>;
